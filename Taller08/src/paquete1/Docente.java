package paquete1;

public class Docente {

    private String nombre;
    private String cedula;

    public void establecerNombre(String n) {
      nombre = n;
    }

    public void establecerCedula(String c) {
        cedula = c;
    }

    public String obtenerNombre() {
        return nombre;
    }

    public String obtenerCedula() {
        return cedula;
    }

   

    }


